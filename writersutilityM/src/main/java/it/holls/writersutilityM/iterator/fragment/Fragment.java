package it.holls.writersutilityM.iterator.fragment;

public class Fragment {

	private String text;
	private int index;
	
	public Fragment(String text, int index){
		this.text = text;
		this.index = index;
	}

	public String getText() {
		return text;
	}

	public int getIndex() {
		return index;
	}
	
	
}
